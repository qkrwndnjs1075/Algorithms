import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        
        Scanner sc = new Scanner(System.in);
        
        int n = sc.nextInt();
        
        for(int i=0; i<n; i++){
            
            int s1= sc.nextInt();
            int s2= sc.nextInt();
            System.out.println(s1 + s2);
        }
        
        
    }
}
